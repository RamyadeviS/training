package com.chainsys.day9;

public class Patterntest {

	public static void main(String[] args) {
		int rows=4;
		for(int i=1;i<=rows;i++) {
			for(int j=1;j<=i;j--) {
			System.out.print("1");
			System.out.print("  0");
			System.out.println("1");
			System.out.print("0");
			System.out.print("1");
			System.out.print("0");
			System.out.print("1");
		}
			System.out.println();
	}

	}
}
